//go:generate sh -c "go run . > ../../../internal/references/references.go"
package main

import (
	"compress/gzip"
	"encoding/json"
	"fmt"
	"os"
	"strings"
)

// estrutura temporária apenas para decodificar o JSON
type rawEntry struct {
	Vector []float64 `json:"vector"`
	Label  string    `json:"label"`
}

func main() {
	var raw []rawEntry

	f, err := os.Open("blobs/references.json.gz")
	if err != nil {
		panic(err)
	}

	reader, err := gzip.NewReader(f)
	if err != nil {
		f.Close()
		panic(err)
	}

	if err := json.NewDecoder(reader).Decode(&raw); err != nil {
		panic(err)
	}
	reader.Close()
	f.Close()

	// cabeçalho do arquivo gerado
	fmt.Println("// Automatically generated by tools/gen/references/references_gen.go, DO NOT EDIT manually")
	fmt.Println("package references")
	fmt.Println()
	fmt.Println("import \"github.com/robogg133/rinha-backend-2026/backend/pkg/vector\"")
	fmt.Println()
	fmt.Print(`type Entry struct {
	Label  uint8
	Vector *vector.Vector
}
`)
	fmt.Println("var References = []Entry{")

	for i, e := range raw {
		// converte label para uint8: legit → 0, fraud → 1 (outros → 0 por segurança)
		var label uint8
		switch e.Label {
		case "fraud":
			label = 1
		default:
			label = 0
		}

		// monta o literal do vetor com floats formatados
		vecParts := make([]string, 14)
		for j, v := range e.Vector {
			vecParts[j] = fmt.Sprintf("%.4f", v)
		}
		vectorLiteral := strings.Join(vecParts, ", ")

		fmt.Printf("\t{Label: %d, Vector: &vector.Vector{%s}},\n", label, vectorLiteral)

		// quebra de linha a cada 10 entradas para legibilidade (opcional)
		if (i+1)%10 == 0 {
			fmt.Println()
		}
	}

	fmt.Println("}")
}
